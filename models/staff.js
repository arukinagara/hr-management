  furigana:             { type: DataTypes.STRING, },
  maidenName:           { type: DataTypes.STRING, field: 'maiden_name', },
  sex:                  { type: DataTypes.STRING, },
  bloodType:            { type: DataTypes.STRING, field: 'blood_type', },
  birthDate:            { type: DataTypes.DATEONLY, field: 'birth_date', },
  joinDate:             { type: DataTypes.DATEONLY, field: 'join_date', },
  employedCategory:     { type: DataTypes.STRING, field: 'employed_category', },
  location:             { type: DataTypes.STRING, },
  projectName:          { type: DataTypes.STRING, field: 'project_name', },
  startDate:            { type: DataTypes.DATEONLY, field: 'start_date', },
  department:           { type: DataTypes.STRING, },
  position:             { type: DataTypes.STRING, },
  staffCategory:        { type: DataTypes.STRING, field: 'staff_category', },
  isLinkage:            { type: DataTypes.BOOLEAN, field: 'is_linkage', },
  staffRank:            { type: DataTypes.STRING, field: 'staff_rank', },
  postalCode:           { type: DataTypes.STRING, field: 'postal_code', },
  address1:             { type: DataTypes.STRING, },
  address2:             { type: DataTypes.STRING, },
  address3:             { type: DataTypes.STRING, },
  phoneNumber:          { type: DataTypes.STRING, field: 'phone_number', },
  mobileNumber:         { type: DataTypes.STRING, field: 'mobile_number', },
