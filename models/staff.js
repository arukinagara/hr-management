  line3:                { type: DataTypes.STRING, },
  station3:             { type: DataTypes.STRING, },
  emergencyFullName:    { type: DataTypes.STRING, field: 'emergency_full_name', },
  emergencyFurigana:    { type: DataTypes.STRING, field: 'emergency_furigana', },
  emergencyRelation:    { type: DataTypes.STRING, field: 'emergency_relation', },
  emergencyIsLiveWith:  { type: DataTypes.BOOLEAN, field: 'emergency_is_live_with', },
  emergencyPostalCode:  { type: DataTypes.STRING, field: 'emergency_postal_code', },
